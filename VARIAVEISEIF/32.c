// Descrição de números

#include <stdio.h>
int main()
{
    int numero;
    printf("Digite um numero:\n");
    scanf("%i", &numero);
    if (numero > 10)
    {
        printf("Desconhecido\n\n\n");
    }
    else if (numero == 0)
    {
        printf("Zero\n\n\n\n");
    }
    else if (numero == 1)
    {
        printf("Um\n\n\n\n");
    }
    else if (numero == 2)
    {
        printf("Dois\n\n\n\n");
    }
    else if (numero == 3)
    {
        printf("Três\n\n\n\n");
    }
    else if (numero == 4)
    {
        printf("Quatro\n\n\n\n");
    }
    else if (numero == 5)
    {
        printf("Cinco\n\n\n\n");
    }
    else if (numero == 6)
    {
        printf("Seis\n\n\n\n");
    }
    else if (numero == 7)
    {
        printf("Sete\n\n\n");
    }
    else if (numero == 8)
    {
        printf("Oito\n\n\n");
    }
    else if (numero == 9)
    {
        printf("Nove\n\n\n");
    }
    else if (numero == 10)
    {
        printf("Dez\n\n\n");
    }
        return 0;
}